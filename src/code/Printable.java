/**
 * Interface That prints every instance variable
 * (including those in parent classes) in a sentence.
 *
 * @author Manases Villalobos
 * @author Darrel Tapilaha
 * @version 1.0
 */
public interface Printable {

    /**
     * Prints all the instance variables
     * (including those in a parent class).
     */
    void display();
}
